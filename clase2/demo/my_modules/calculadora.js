const calculadora = {
    sumar: function(valorA, valorB) {
        return valorA + valorB;
    },
    restar: function(valorA, valorB) {
        return valorA - valorB;
    },
    multi: function(valorA, valorB) {
        return valorA * valorB;
    },
    div: function(valorA, valorB) {
        return valorA / valorB;
    },
}

module.exports = calculadora;